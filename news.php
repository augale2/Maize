            <p class="text-sm text-gray-600">Feb 7, 2025</p>
            <p class="text-gray-700 text-base">Gibberellic acid (GA) is a phytohormone that is important for plant growth and development. Mutants in GA biosynthesis, signaling and metabolism have been critical to understanding the role GA plays in plants. GA mutants have also revolutionized global production of staple crops such as rice, wheat, and barley. GA mutants have been isolated in maize and characterization of the underlying genes has helped map the GA biosynthesis and signaling pathways. However, the number of maize dwarf mutants is far less than other species. Here, we identify new dwarf mutants that could benefit our understanding of maize plant height control.</p>
