            <a href="https://pubmed.ncbi.nlm.nih.gov/38198228/" target="_blank" class="text-[#14b02a] font-semibold hover:underline">
                The maize PLASTID TERMINAL OXIDASE (PTOX) locus controls the carotenoid content of kernels
            </a>
            <p class="text-sm text-gray-600">Apr 18, 2024</p>
            <p class="text-gray-700 text-base">Carotenoids perform a broad range of important functions in humans; therefore, carotenoid biofortification of maize (Zea mays L.), one of the most highly produced cereal crops worldwide, would have a global impact on human health. PLASTID TERMINAL OXIDASE (PTOX) genes play an important role in carotenoid metabolism; however, the possible function of PTOX in carotenoid biosynthesis in maize has not yet been explored. In this study, we characterized the maize PTOX locus by forward- and reverse-genetic analyses. While most higher plant species possess a single copy of the PTOX gene, maize carries two tandemly duplicated copies. Characterization of mutants revealed that disruption of either copy resulted in a carotenoid-deficient phenotype. We identified mutations in the PTOX genes as being causal of the classic maize mutant, albescent1. Remarkably, overexpression of ZmPTOX1 significantly improved the content of carotenoids, especially Î²-carotene (provitamin A), which was increased by ~threefold, in maize kernels. Overall, our study shows that maize PTOX locus plays an important role in carotenoid biosynthesis in maize kernels and suggests that fine-tuning the expression of this gene could improve the nutritional value of cereal grains.</p>
