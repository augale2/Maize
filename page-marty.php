    <section class="bg-white p-6 shadow-md rounded-lg">
        <h2 class="text-3xl font-semibold">Description of Research</h2>
        <p class="text-gray-700 mt-4">
        Oxygen limitation is the primary stress that plants experience in flooded soils. About 20 million acres of corn and soybeans were inundated, leading to heavy economic losses, during 1993 flooding in the Mid-Western United States. The research in my laboratory involves the analysis of the anaerobic-stress response in Zea mays (maize, corn). 
        The anaerobic response is most likely a plant's natural reaction to flooding. Anaerobic treatment dramatically alters the pattern of gene expression in maize seedlings. During anaerobiosis there is an immediate repression of normal 'aerobic' protein synthesis and the simultaneous selective synthesis of 
        four polypeptides with a MW of ~33 kDa (the transition polypeptides). After 90 min, this is followed by specific transcription and mRNA accumulation as well as the selective synthesis of approximately twenty anaerobic-specific proteins. Among these anaerobic proteins (ANPs) are enzymes involved in glycolysis and related processes, 
        such as alcohol dehydrogenase (ADH), aldolase, enolase, glucose phosphate isomerase, glyceraldehyde-3-phosphate dehydrogenase, pyruvate decarboxylase, and sucrose synthase. However, we also found two inducible genes that have different functions. One that has homology to xyloglucan endotransglycosylase may be 
        involved in aerenchyma (cortical air space) formation during flooding (allowing transport of oxygen from the leaves to the submerged, oxygen deprived, roots of a plant). My laboratory has been analyzing the anaerobic response and flooding tolerance in maize using a combination of molecular, biochemical and genetic approaches. 
        Our studies include the characterization of anaerobically inducible genes, the identification of genes that confer increased flooding (anaerobic) tolerance in maize, and analysis of the signal transduction events that are involved in perception of oxygen deprivation and lead to the anaerobic response.
        </p>
